function arpTable(rows: Array<{ IP: string; MAC: string; Meaning: string }>) {
  return {
    label: 'H1 ARP Table',
    columns: ['IP', 'MAC', 'Meaning'],
    rows,
  }
}

function macTable(rows: Array<{ MAC: string; Port: string; Meaning: string }>) {
  return {
    label: 'Switch S MAC Table',
    columns: ['MAC', 'Port', 'Meaning'],
    rows,
  }
}

